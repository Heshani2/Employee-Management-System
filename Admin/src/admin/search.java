    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         String Search = Search_main.getText();
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                String s ="SELECT * FROM search";
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studiesinstitute","root","");
                pst = con.prepareStatement(s);
                pst.setString(1,Search);
                pst.setString(2,Search);
                pst.setString(3,Search);
                pst.setString(4,Search);
                rs = pst.executeQuery();
                DefaultTableModel d = (DefaultTableModel)search_table.getModel();
                d.setRowCount(0);
                while(rs.next()){
                    String emp_id = rs.getString("ID");
                    String dep_name = rs.getString("Department_name");
                    String desig_name = rs.getString("Designation_name");
                    String emp_name = rs.getString("employee_name");
                    String epf_no = rs.getString("EPF_no");
                    String[] tb ={emp_id,dep_name,desig_name,emp_name,epf_no};
                    d.addRow(tb);
                                       
                }

            }catch(Exception ex){
                java.util.logging.Logger.getLogger(search.class.getName()).log(Level.SEVERE,null,ex);

            }

    }//GEN-LAST:event_jButton3ActionPerformed

